package FlowControl;

public class IterativeBreak {
	
	

	public static void main(String[] args) {
	
  for(int i=0,j=5;i<6;i++,j--) {
	  if(i==5)
		  break;
		   
			
		   
		    
	System.out.println("Values of j are  " +j);
	
	System.out.println("Numbers of i are " +i);
	
		   }}
}
